		<h2>和你使用windows的‘我的电脑’的方式使用</h2>
		<li>快捷键：选中后 ctrl+c--复制，delete--删除，ctrl+x--剪切，F2--重命名……</li>
		<li>右键：body，文件，文件夹上分别使用不同的功能</li>
		<li>地址栏：修改或者填写地址，enter或者点击按钮即可。</li>
		<li>前进后退：能记录打开的文件夹历史记录，可以点击按钮前进后退，或者backspace后退</li>
		<li>树目录：可以无限展开，点击即可定位到文件夹。</li>
	
		<li>操作，一切都那么便捷！</li>
